#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*aux;

	if (lst && del)
	{
		aux = *lst;
		while (aux != NULL)
		{
			*lst = aux->next;
			ft_lstdelone(aux, del);
			aux = *lst;
		}
		lst = NULL;
	}
}
